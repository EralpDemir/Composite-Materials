function [ke] = FEM_elstiff(A,B,D,eltyp,crds,conn,numind,nqpts,iele)
% FEM_elstiff calculates element stiffness

% A-matrix
A11 = A(1); A12 = A(2); A22 = A(3);
A16 = A(4); A26 = A(5); A66 = A(6);
% B-matrix
B11 = B(1); B12 = B(2); B22 = B(3);
B16 = B(4); B26 = B(5); B66 = B(6);
% D-matrix
D11 = D(1); D12 = D(2); D22 = D(3);
D16 = D(4); D26 = D(5); D66 = D(6);
% % H-matrix
% H44 = H(1); H45 = H(2); H55 = H(3);

A3x3 = [A11 A12 A16;
    A12 A22 A26;
    A16 A26 A66];

D3x3 = [D11 D12 D16;
    D12 D22 D26;
    D16 D26 D66];

switch eltyp
    
    case 'TRI'
        
        % Quadrature points and their weights
        [k, e, w] = FEM_quad_trgl(nqpts);
        
        for iqpt = 1:1:nqpts
            
            ksi = k(iqpt);
            eta = e(iqpt);
            
            % Compute shape functions in parametric coordinates
            [~,dN,area] = FEM_nmat(ksi,eta,crds,conn,eltyp,iele);
            
            % Calculate strain-displacement matrix
            [S] = FEM_bmat(dN);
            
            % Calculate elemental stiffness matric
            ke = S'*A3x3*S*w(iqpt)*area;
            
        end
        
    case 'HCT'
        
        % Global node numbers for each element
        elnodes = conn(iele,:);
        
        % Node coordinates for each element global nodes regarding connectivity
        elcrds(:,:) = crds(elnodes,:);
        
        % First 3 coordinates are the vertex coordinates for each element
        % Note that these are local nodes in each element. global node 3 1 2 6 4 5
        % is related to local node 1 2 3 4 5 6
        
        x1 = elcrds(1,1);
        y1 = elcrds(1,2);
        x2 = elcrds(2,1);
        y2 = elcrds(2,2);
        x3 = elcrds(3,1);
        y3 = elcrds(3,2);
        
        % Compute quad point locations
        [xi, eta, w] = FEM_quad_trgl(nqpts);
        
        % compute the area of the triangle
        d12x = x1-x2; d12y = y1-y2;
        d31x = x3-x1; d31y = y3-y1;
        area = 0.5*(d31x*d12y - d31y*d12x); % area of each element
        
        % interior node
        x7 = (x1+x2+x3)/3.0;
        y7 = (y1+y2+y3)/3.0;
        
        % initialize the element bending stiffness matrix
        % and right-hand side
        ke = zeros(numind);
        
        % Deformation modes
        for mode = 1:numind
            
            dofs = zeros(1,numind);
            
            dofs(mode) = 1.0;
            
            [a] = FEM_hctsys(x1,y1, x2,y2, x3,y3, dofs);
            
            for i = 1:30
                am(i,mode) = a(i);
            end
            
        end
        
        % Sub-triangles
        for isub = 1:3
            
            % Quad points of each sub-element
            for iquad = 1:nqpts   % numerical quadrature
                
                lpsi = zeros(3,numind);
                
                % For each deformation mode
                for imode = 1:numind
                    
                    c(:,1) = am(:,imode);
                    
                    if isub == 1       % triangle 1-2-7
                        
                        x = x1 + (x2-x1)*xi(iquad) + (x7-x1)*eta(iquad);
                        y = y1 + (y2-y1)*xi(iquad) + (y7-y1)*eta(iquad);
                        
                        lpsi(1:3,imode) = [   c(4)*2 + c(7)*6*x + c(8)*2*y;
                            c(6)*2 + c(9)*2*x + c(10)*6*y;
                            2*c(5) + 2*c(8)*2*x + 2*c(9)*2*y];
                        
                        
                    elseif isub == 2  % triangle 2-3-7
                        
                        x = x2 + (x3-x2)*xi(iquad) + (x7-x2)*eta(iquad);
                        y = y2 + (y3-y2)*xi(iquad) + (y7-y2)*eta(iquad);
                        
                        lpsi(1:3,imode) = [   c(14)*2 + c(17)*6*x + c(18)*2*y;
                            c(16)*2 + c(19)*2*x + c(20)*6*y;
                            2*c(15) + 2*c(18)*2*x + 2*c(19)*2*y];
                        
                        
                    else            % triangle 3-1-7
                        
                        x = x3 + (x1-x3)*xi(iquad) + (x7-x3)*eta(iquad);
                        y = y3 + (y1-y3)*xi(iquad) + (y7-y3)*eta(iquad);
                        
                        lpsi(1:3,imode) = [ c(24)*2 + c(27)*6*x + c(28)*2*y;
                            c(26)*2 + c(29)*2*x + c(30)*6*y;
                            2*c(25) + 2*c(28)*2*x + 2*c(29)*2*y];
                        
                    end
                    
                end
                
                % Area and weight of integraion of each subtriangle
                cf = area*w(iquad)/3.0;
                
                for k = 1:numind
                    
                    for l = 1:numind
                        
                        ke(k,l) = ke(k,l) + lpsi(:,k)'*D3x3*lpsi(:,l)*cf;
                        
                    end
                    
                end
                
            end
            
        end
        
        
    case 'REC'
        
        % Dimensions of the element
        a = abs(crds(conn(iele,2),1) - crds(conn(iele,1),1))/2;
        b = abs(crds(conn(iele,4),2) - crds(conn(iele,1),2))/2;
        
        ke = zeros(8,8);
        
        ke(:,1) = [
            A16/2 + (A11*b)/(3*a) + (A66*a)/(3*b);
            A12/4 + A66/4 + (A16*b)/(3*a) + (A26*a)/(3*b);
            (A66*a)/(6*b) - (A11*b)/(3*a);
            A12/4 - A66/4 - (A16*b)/(3*a) + (A26*a)/(6*b);
            - A16/2 - (A11*b)/(6*a) - (A66*a)/(6*b);
            - A12/4 - A66/4 - (A16*b)/(6*a) - (A26*a)/(6*b);
            (A11*b)/(6*a) - (A66*a)/(3*b);
            A66/4 - A12/4 + (A16*b)/(6*a) - (A26*a)/(3*b);
            ];
        
