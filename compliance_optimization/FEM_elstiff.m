            (((18*D12*a^2)/25 + (36*D66*a^2)/25)*b^2 - (27*D22*a^4)/70)/(a^3*b^3) - (27*D11*b)/(70*a^3);
            (65*D22*a^4 + 135*D11*b^4 - 42*D12*a^2*b^2 - 84*D66*a^2*b^2 + 350*D16*a*b^3)/(350*a^2*b^3);
            (135*D22*a^4 + 65*D11*b^4 - 42*D12*a^2*b^2 - 84*D66*a^2*b^2 + 350*D26*a^3*b)/(350*a^3*b^2);
            D12/50 + D66/25 - (13*D11*b^2)/(70*a^2) - (13*D22*a^2)/(70*b^2) - (2*D16*b)/(5*a) - (2*D26*a)/(5*b);
            (27*D11*b)/(70*a^3) - (b^2*((18*D12*a^2)/25 + (36*D66*a^2)/25) + (39*D22*a^4)/35)/(a^3*b^3);
